  constructor(
    public themeService: ThemeService,
    @Inject(PLATFORM_ID) platformId: Object
  ) {
    this.isBrowser = isPlatformBrowser(platformId);
    
    // Load sidebar state from localStorage (only in browser)
    if (this.isBrowser) {
      const savedState = localStorage.getItem('sidebarMini');
      if (savedState !== null) {
        this.isMiniSidebar = savedState === 'true';
      }
    }
  }

  toggleSidebar(): void {
    this.isMiniSidebar = !this.isMiniSidebar;
    // Save state to localStorage (only in browser)
    if (this.isBrowser) {
      localStorage.setItem('sidebarMini', this.isMiniSidebar.toString());
    }
  }

  toggleMobileSidebar(): void {
    this.isMobileSidebarOpen = !this.isMobileSidebarOpen;
  }

  toggleTheme(): void {
    this.themeService.toggleTheme();
  }

  getIconPaths(icon: string): string[] {
    const iconKey = icon.toLowerCase();
    return this.icons[iconKey] || this.icons['home'];
  }
